float calculate_pressure_physics(uint32_t adc_raw){
	// 1. Constants
	const float VCC = 3.3;          // System voltage
	const float R_FIXED = 2000.0;  // 2k resistor
	const float AREA = 0.00188;     // 18.8 cm^2 in m^2

	// CALIBRATION FACTOR 'k'
	// Increase this if pressure is too low, decrease if too high.
	// Ideally found by: (Known_Weight_N * Resistance_at_Weight)
	const float k_calibration = 1.0;

	// 2. Avoid division by zero
	if(adc_raw >= 4090) return 0.0;
	if(adc_raw == 0) adc_raw =1;

	// 3. Convert ADC to Voltage
	float voltage = (adc_raw * VCC) / 4095.0;

	// 4. Calculate Resistance for PULL-UP Configuration
	// In Pull-Up: Voltage drops as Resistance drops.
	float r_sensor = R_FIXED * (voltage / (VCC - voltage));

	// 5. Calculate Force (F = k / R)
	// As r_sensor gets smaller (pressed), force gets bigger.
	float force_newtons = k_calibration / (r_sensor / 2000.0); // Scaled by 2k for stability

	// 6. Calculate Pressure (P = F / A)
	float pressure_pa = force_newtons / AREA;

	return pressure_pa;
}



float kalman_filter(float input_value)
{
	static float x_est = 0;
	static float P = 1.0;
	static const float Q = 10.0;  // Process noise (Pressure can change fast)
	static const float R = 100.0; // Sensor noise (Foam is very noisy!)

	float Kg;

	// Prediction
	P = P + Q;

	// Update
	Kg = P / (P + R);
	x_est = x_est + Kg * (input_value - x_est);
	P = (1 - Kg) * P;

	return x_est;
}
